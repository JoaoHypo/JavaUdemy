package entities;

public class ProductVect {

    private final String  name;
    private double price;

    public ProductVect(String name,double price){
        this.name = name;
        this.price = price;
    }

    public String getName() {
        return name;
    }
    public double getPrice() {
        return price;
    }
    public double setPrice() {
        return price;
    }

}
